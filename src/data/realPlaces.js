function fallbackPlaceDetail(step, route) {
  const keyword = step.amapKeyword || `${route.destination} ${step.place}`;
  return {
    type: /地铁|返程|入口|站/.test(step.place + step.action) ? "transport" : /餐|吃|小吃|简餐|饮品|便利店/.test(step.place + step.action) ? "food" : "activity",
    address: `${route.destination}附近，建议打开地图搜索确认具体位置`,
    district: "北京市",
    nearestSubway: route.destination,
    lat: undefined,
    lng: undefined,
    amapKeyword: keyword,
    openTime: "以地图搜索结果为准",
    estimatedStay: step.cost > 0 ? "30-60分钟" : "30-90分钟",
    whyRecommended: step.tip || route.whyRecommended
  };
}

function inferCostType(step, detail) {
  if (step.costType) return step.costType;
  if (detail.type === "transport") return "transport";
  if (detail.type === "food" || /餐|吃|小吃|轻食|简餐|饮品|便利店|补给/.test(step.action + step.place)) return "food";
  if (/票|门票/.test(step.action + step.place)) return "ticket";
  if (detail.type === "activity") return "activity";
  return "other";
}

export function enrichRouteSteps(route) {
  return {
    ...route,
    ...(routeRegionalProfiles[route.routeId] || {}),
    steps: route.steps.map((step, index) => {
      const detail = placeDetails[step.place] || fallbackPlaceDetail(step, route);
      return {
        id: `${route.routeId}-step-${index + 1}`,
        type: detail.type,
        place: step.place,
        action: step.action,
        tip: step.tip,
        cost: step.cost,
        address: detail.address,
        district: detail.district,
        nearestSubway: detail.nearestSubway,
        lat: detail.lat,
        lng: detail.lng,
        amapKeyword: detail.amapKeyword,
        openTime: detail.openTime,
        estimatedStay: detail.estimatedStay,
        whyRecommended: detail.whyRecommended,
        costType: inferCostType(step, detail),
        copyText: `${step.place}：${step.action}。地址：${detail.address}；最近地铁：${detail.nearestSubway}；预计花费：${step.cost}元。`
      };
    })
  };
}
